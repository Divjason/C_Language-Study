#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

int main()
{
	int num, sum = 0;
	int status;

	printf("Enter an Integer(q to quit : ");

	status = scanf("%d", &num);

	while (status == 1) {
		sum += num;

		printf("Enter an Integer(q to quit : ");
		status = scanf("%d", &num);
	}

	printf("Sum = %d\n", sum);

	return 0;
}