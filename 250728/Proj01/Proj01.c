// 4. 사용자가 입력한 값이 만약 정수이면 출력과 동시에 빈 변수 할당
// 5. 변수에 할당된 값을 하나씩 더해간다
// 6. 만약, 사용자가 q라는 문자를 입력하면, 더이상 값을 출력하지않는다
